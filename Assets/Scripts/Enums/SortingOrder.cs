public enum SortingOrder {
    None,
    TopToBottom,            // This means higher y value draws on top
    TopRightToBottomLeft,    // This means the greater the y and the greater amount of x is drawn first
    BottomToTop,            // This means lesser y value draws on top
    BottomLeftToTopRight    // This means the lesser the y and the lesser amount of x is drawn first
}
